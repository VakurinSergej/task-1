// По заданному номеру дня недели вывести его название

Console.Write("Введите число от 1 до 7: ");
string num = Console.ReadLine();

if (num == "1")
{
    Console.WriteLine("Сегодня monday");
}
if (num == "2")
{
    Console.WriteLine("Сегодня tuesday");
}
if (num == "3")
{
    Console.WriteLine("Сегодня wednesday");
}
if (num == "4")
{
    Console.WriteLine("Сегодня thursday");
}
if (num == "5")
{
    Console.WriteLine("Сегодня friday");
}
if (num == "6")
{
    Console.WriteLine("Сегодня saturday");
}
if (num == "7")
{
    Console.WriteLine("Сегодня sunday");
}